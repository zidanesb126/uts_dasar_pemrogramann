#include <iostream>
using namespace std;

int main()
{
    int i,j;
    i = 5;
    cout << "nilai i awal  : " << i << endl;
    cout << "nilai ++i     : " << ++i << endl;
    cout << "niali i akhir : " << i << endl << endl;

    j = 10;
    cout << "nilai j awal  : " << j << endl;
    cout << "nilai ++j     : " << ++j << endl;
    cout << "niali j akhir : " << j << endl << endl;

    return 0;

}
