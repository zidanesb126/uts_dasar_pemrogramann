#include <iostream>
using namespace std;

int main()
{
    cout << "2 + 3 = " << 2 + 3 << endl << endl;
    cout << "10 - 5 = " << 10 - 5 << endl << endl;
    cout << "4 x 3 = " << 4 * 3 << endl << endl;
    cout << "4/2 = " << 4 / 2 << endl << endl;
    cout << "10 % 3 = " << 10 % 3 << endl << endl;

    return 0;
}
