#include <iostream>
using namespace std;

int main()
{
    int x = 82, y = 26;
    cout << x << "+" << y << " = "<<x+y << endl;
    cout << x << "-" << y << " = "<<x-y << endl;
    cout << x << "*" << y << " = "<<x*y << endl;
    cout << x << "/" << y << " = "<<x/y << endl;
    cout << x << "%" << y << " = "<<x%y << endl;
    cout << "-" << x << " = " << -x << endl;

    return 0;
}
