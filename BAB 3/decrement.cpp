#include <iostream>
using namespace std;

int main()
{
    int k;
    float l;

    k = 100;
    l = 10.5;

    cout << "nilai k awal    : " << k << endl;
    cout << "nilai --k       : " << --k << endl;
    cout << "nilai k akhir   : " << k << endl;

    cout << "nilai l awal    : " << l << endl;
    cout << "nilai --l       : " << --l << endl;
    cout << "nilai l akhir   : " << l << endl;

    return 0;

}
