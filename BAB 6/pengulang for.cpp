#include <iostream>
using namespace std;

int main()
{
    // pengulang menaik
    cout << "PENGULANGAN MENAIK" << endl;
    for (int C=0; C<10; C++)
    {
        cout << C+1 << endl;
    }
    cout << "\n";
    // pengulang menurun
    cout << "PENGULANG MENURUN" << endl;
    for (int D=10; D>0; D--)
    {
        cout << D << endl;
    }
    return 0;
}
